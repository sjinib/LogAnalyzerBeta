                settingsFileStatus.setText("Settings File - Not Selected");
                settingsFileStatus.setForeground(Color.RED);
                localManager.setIncludeXml(false);
                includeXmlCheck.setSelected(false);
                includeXmlCheck.setEnabled(false);
